<?php

$arr = [1, 2, 3, 3, 4, 5, 5];

$flag = false; //використання флага

//цикл пошуку сусідніх елементів масиву
foreach ($arr as $key=>$elem) {
    if ($key > 0 and $elem == $arr[$key - 1]) {  //звертання до сусідніх елементів масиву
        $flag = true;
        break;
    }
}

//виведення відповіді
if ($flag == true) {
    echo 'да';       //є сусідні елементи
} else {
    echo 'нет';      //немає сусідніх елементів
}
?>